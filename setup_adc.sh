#inputs
sudo i2cset -y 1 0x4a 0x06 0x48
sudo i2cset -y 1 0x4a 0x07 0x48
sudo i2cset -y 1 0x4a 0x08 0x40
sudo i2cset -y 1 0x4a 0x09 0x40
#pregain
sudo i2cset -y 1 0x4a 0x01 0x00
sudo i2cset -y 1 0x4a 0x02 0x00
sudo i2cset -y 1 0x4a 0x03 0x00
sudo i2cset -y 1 0x4a 0x04 0x00
#offset
sudo i2cset -y 1 0x4a 0x0d 0x00

