#!/bin/sh
# clock master mode autodetect
sudo i2cset -y 1 0x4a 0x20 0x11
#Recieve PCM word length set tdm mode
#sudo i2cset -y 1 0x4a 0x0B 0xCF
sudo i2cset -y 1 0x4a 0x0B 0x00
#4ch
#sudo i2cset -y 1 0x4a 0x0C 0x01
